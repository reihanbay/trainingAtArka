package com.reihan.latihanarka1.oop.kucing

/**
 * Inheritance Concept 2
 * */

open class kucing(){
    //Deklarasi Atribut
    var bulu = "berbulu"
    var kaki = 4

    //Deklarasi Fungsi\

    fun berbulu(){
        println("Kucing $bulu")
    }

    fun berkaki(){
        println("Kucing berkaki $kaki")
    }
}